const englishBlockerAliases = {
  "Required market setup is not complete.": "required_setup_incomplete",
};

function normalizeLocale(locale) {
  return platformLocales.includes(locale) ? locale : "tr";
}

export function t(key, locale = "tr") {
  const normalized = normalizeLocale(locale);
  return platformLabels[normalized][key] || platformLabels.tr[key] || key;
}

export function statusLabel(value, locale = "tr") {
  const normalized = normalizeLocale(locale);
  if (typeof value !== "string" || !value) return t("unknownStatus", normalized);
  return statusLabels[normalized][value] || `${t("unknownStatus", normalized)}: ${value}`;
}

export function blockerCode(blocker) {
  if (typeof blocker === "string") return englishBlockerAliases[blocker] || blocker;
  if (blocker && typeof blocker === "object" && typeof blocker.code === "string") return blocker.code;
  return "unknown";
}

export function blockerLabel(blocker, locale = "tr") {
  const normalized = normalizeLocale(locale);
  const code = blockerCode(blocker);
  return blockerLabels[normalized][code] || `${t("unknownStatus", normalized)}: ${code}`;
}

export function deriveReadiness(market) {
  const blockers = [];
  const productCount = Number(market?.product_count || 0);
  const campaignCount = Number(market?.campaign_count || 0);
  const memberCount = Number(market?.member_count || 0);

  if (market?.lifecycle_status === "suspended" || market?.lifecycle_status === "archived") {
    blockers.push({ code: "market_suspended" });
  }
  if (memberCount < 1) blockers.push({ code: "active_user_missing" });
  if (market?.onboarding_status && market.onboarding_status !== "completed") {
    blockers.push({ code: "onboarding_incomplete" });
  }
  if (productCount < 1) blockers.push({ code: "product_required" });
  if (campaignCount < 1) blockers.push({ code: "campaign_required" });
  if (!market?.legal_name || !market?.country_code || !market?.language || !market?.currency || !market?.timezone) {
    blockers.push({ code: "profile_incomplete" });
  }

  const status = blockers.length === 0 ? "ready" : market?.onboarding_status === "completed" ? "blocked" : "onboarding";
  return { status, blockers };
}

export function countRows(market, locale = "tr") {
  return [
    { label: t("product", locale), value: Number(market?.product_count || 0) },
    { label: t("campaign", locale), value: Number(market?.campaign_count || 0) },
  ];
}
